        <div class="info-box">
            <h2>Apa Itu Sampah Plastik?</h2>
            <p><strong>Sampah plastik</strong> adalah limbah yang terdiri dari bahan plastik, mencakup berbagai jenis polimer sintetik dan semi-sintetik. Plastik adalah bahan yang sangat serbaguna dan digunakan dalam berbagai aplikasi di kehidupan sehari-hari. Namun, karena sifatnya yang tidak mudah terurai secara alami, sampah plastik menjadi masalah lingkungan yang serius.</p>
        </div>
        <div class="info-box">
            <h2>Jenis-Jenis Plastik</h2>
            <ul>
                <li><strong>Polietilena Tereftalat (PET):</strong> Digunakan untuk botol minuman, wadah makanan, dan serat sintetis.</li>
                <li><strong>Polietilena Densitas Tinggi (HDPE):</strong> Digunakan untuk botol susu, deterjen, mainan, dan pipa.</li>
                <li><strong>Polivinil Klorida (PVC):</strong> Digunakan untuk pipa, kabel listrik, dan produk medis.</li>
                <li><strong>Polipropilena (PP):</strong> Digunakan untuk sedotan, tutup botol, peralatan laboratorium, dan tekstil.</li>
                <li><strong>Polistirena (PS):</strong> Digunakan untuk peralatan makan sekali pakai, kemasan makanan, dan bahan isolasi.</li>
                <li><strong>Polietilena Densitas Rendah (LDPE):</strong> Digunakan untuk kantong plastik, bungkus plastik, dan botol semprot.</li>
                <li><strong>Polikarbonat (PC):</strong> Digunakan untuk botol minuman keras, peralatan laboratorium, dan komponen elektronik.</li>
                <li><strong>Poliamida (Nylon):</strong> Digunakan untuk tekstil, tali, jaring, dan komponen mekanik.</li>
                <li><strong>Poliakrilonitril (PAN):</strong> Digunakan untuk serat tekstil, filamen, dan bahan komposit.</li>
                <li><strong>Polibutilena Tereftalat (PBT):</strong> Digunakan untuk komponen otomotif, elektronik, dan alat kelistrikan.</li>
                <li><strong>Politetrafluoroetilena (PTFE):</strong> Digunakan untuk lapisan anti lengket pada alat masak, segel, dan isolasi kabel.</li>
                <li><strong>Akrilonitril Butadiena Stiren (ABS):</strong> Digunakan untuk mainan, komponen otomotif, dan casing elektronik.</li>
            </ul>
        </div>
        <div class="info-box">
            <h2>Dampak Lingkungan</h2>
            <p>Sampah plastik yang tidak terkelola dengan baik dapat mencemari lingkungan, mengancam kehidupan laut, dan masuk ke dalam rantai makanan manusia. Plastik yang terurai menjadi mikroplastik dapat mencemari air dan tanah, yang dapat memiliki dampak jangka panjang pada kesehatan ekosistem dan manusia.</p>
            <p class="highlight">Fakta: Setiap tahun, jutaan ton sampah plastik berakhir di lautan, mengancam kehidupan laut.</p>
